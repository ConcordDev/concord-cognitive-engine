// server/lib/cross-world-feed.js
//
// Phase P — cross-world federation surface.
//
// Concord-link-frontier becomes the news feed of every world. Pulls notable
// events (DTU promotions, faction wars, big combat, deaths of authored NPCs)
// from every active world, sorted by recency × notability.
//
// Also tracks cross-world royalty flow — citations where parent and child
// DTUs live in different worlds. The cascade already flows cross-world
// (dtu_citations doesn't gate on world_id); this surface makes it visible.

import logger from "../logger.js";

const NOTABILITY_WEIGHTS = {
  "dtu:promoted":       3.0,
  "faction-war:started": 4.0,
  "faction-war:ended":  3.5,
  "combat:hit":         1.0,
  "entity:death":       2.0,
  "npc:legacy":         2.5,
  "world:event:scheduled": 1.5,
  "world:crisis":       3.5,
  "lattice:meta:emerged": 4.0,
};

/**
 * Get notable events across all active worlds.
 *
 * @param {object} db
 * @param {object} opts
 * @param {number} [opts.limit=50]
 * @param {number} [opts.sinceMs=3_600_000] - 1h default
 * @param {string} [opts.kindFilter] - optional event kind filter
 * @returns {{ events: Array, worlds: number, generatedAt: number }}
 */
export function getCrossWorldFeed(db, opts = {}) {
  const limit = Math.min(Math.max(1, opts.limit || 50), 200);
  const sinceMs = Math.max(60_000, opts.sinceMs || 3_600_000);
  const since = Math.floor((Date.now() - sinceMs) / 1000);
  const events = [];

  if (!db) return { events, worlds: 0, generatedAt: Date.now() };

  // DTU promotions across all worlds.
  try {
    const rows = db.prepare(`
      SELECT id, world_id, title, type AS kind, creator_id AS created_by, created_at,
             COALESCE(data, '{}') AS meta_json
      FROM dtus
      WHERE tier IN ('mega','hyper') AND created_at >= ?
      ORDER BY created_at DESC
      LIMIT 100
    `).all(since);
    for (const r of rows) {
      events.push({
        kind: "dtu:promoted",
        worldId: r.world_id || "concordia-hub",
        ts: r.created_at,
        summary: `DTU "${r.title || r.id}" was promoted to ${r.kind}`,
        ref: { dtuId: r.id, creatorId: r.created_by },
        notability: NOTABILITY_WEIGHTS["dtu:promoted"] || 1.0,
      });
    }
  } catch { /* dtus table absent in minimal builds */ }

  // Faction war activity.
  try {
    const rows = db.prepare(`
      SELECT faction_id, action, target_id, world_id, ts
      FROM faction_strategy_log
      WHERE ts >= ? AND action IN ('DECLARE_WAR', 'SEEK_TRUCE', 'PROPOSE_ALLIANCE', 'RAID')
      ORDER BY ts DESC LIMIT 50
    `).all(since);
    for (const r of rows) {
      const kind = r.action === "DECLARE_WAR" ? "faction-war:started"
                : r.action === "SEEK_TRUCE"   ? "faction-war:ended"
                : "faction-strategy:move";
      events.push({
        kind, worldId: r.world_id || "concordia-hub", ts: r.ts,
        summary: `${r.faction_id} ${r.action.replace(/_/g, " ").toLowerCase()}${r.target_id ? ` ${r.target_id}` : ""}`,
        ref: { factionId: r.faction_id, targetId: r.target_id },
        notability: NOTABILITY_WEIGHTS[kind] || 1.5,
      });
    }
  } catch { /* table optional */ }

  // NPC legacy events (death of authored characters).
  try {
    const rows = db.prepare(`
      SELECT npc_id, world_id, died_at, cause, last_words
      FROM npc_legacies
      WHERE died_at >= ?
      ORDER BY died_at DESC LIMIT 30
    `).all(since);
    for (const r of rows) {
      events.push({
        kind: "npc:legacy", worldId: r.world_id || "concordia-hub", ts: r.died_at,
        summary: `${r.npc_id} died (${r.cause}). Last words: "${(r.last_words || "").slice(0, 80)}"`,
        ref: { npcId: r.npc_id, cause: r.cause },
        notability: NOTABILITY_WEIGHTS["npc:legacy"] || 2.5,
      });
    }
  } catch { /* table optional */ }

  // World events (auto-generated by world-event-scheduler).
  try {
    const rows = db.prepare(`
      SELECT id, world_id, kind, title, created_at
      FROM world_events
      WHERE created_at >= ?
      ORDER BY created_at DESC LIMIT 30
    `).all(since);
    for (const r of rows) {
      events.push({
        kind: "world:event:scheduled", worldId: r.world_id, ts: r.created_at,
        summary: `${r.kind}: "${r.title}"`,
        ref: { eventId: r.id },
        notability: NOTABILITY_WEIGHTS["world:event:scheduled"] || 1.5,
      });
    }
  } catch { /* table optional */ }

  // Sort by recency × notability — newest events with highest notability first.
  events.sort((a, b) => {
    // Score: notability * (1 / (1 + age_in_hours))
    const ageA = Math.max(0, (Date.now() / 1000 - a.ts) / 3600);
    const ageB = Math.max(0, (Date.now() / 1000 - b.ts) / 3600);
    const scoreA = a.notability / (1 + ageA);
    const scoreB = b.notability / (1 + ageB);
    return scoreB - scoreA;
  });

  const filtered = opts.kindFilter
    ? events.filter(e => e.kind === opts.kindFilter || e.kind.startsWith(`${opts.kindFilter}:`))
    : events;

  const worlds = new Set(events.map(e => e.worldId));
  return {
    events: filtered.slice(0, limit),
    worlds: worlds.size,
    generatedAt: Date.now(),
  };
}

/**
 * Get cross-world royalty flow — citations where parent + child DTUs live
 * in different worlds. Visualises Phase P's "fantasy wizard cites cyber
 * hacker, royalties cascade across worlds" claim.
 */
export function getCrossWorldRoyaltyFlow(db, opts = {}) {
  if (!db) return { flows: [], totalRoyaltyCC: 0, generatedAt: Date.now() };
  const limit = Math.min(Math.max(1, opts.limit || 50), 200);
  const sinceMs = Math.max(60_000, opts.sinceMs || 86_400_000);
  const since = Math.floor((Date.now() - sinceMs) / 1000);

  try {
    const rows = db.prepare(`
      SELECT
        c.id AS citation_id,
        c.parent_id AS parent_dtu_id, c.child_id AS child_dtu_id, c.created_at,
        p.world_id AS parent_world_id, p.title AS parent_title, p.creator_id AS parent_creator,
        d.world_id AS child_world_id,  d.title AS child_title,  d.creator_id AS child_creator,
        l.amount AS amount_cc, l.created_at AS payout_ts
      FROM royalty_lineage c
      JOIN dtus p ON p.id = c.parent_id
      JOIN dtus d ON d.id = c.child_id
      LEFT JOIN economy_ledger l ON l.ref_id = c.id AND l.type = 'ROYALTY_PAYOUT'
      WHERE c.created_at >= datetime(?, 'unixepoch')
        AND COALESCE(p.world_id, 'concordia-hub') != COALESCE(d.world_id, 'concordia-hub')
      ORDER BY c.created_at DESC
      LIMIT ?
    `).all(since, limit);

    const flows = rows.map(r => ({
      citationId: r.citation_id,
      parentDtuId: r.parent_dtu_id,
      parentTitle: r.parent_title,
      parentWorldId: r.parent_world_id || "concordia-hub",
      parentCreator: r.parent_creator,
      childDtuId: r.child_dtu_id,
      childTitle: r.child_title,
      childWorldId: r.child_world_id || "concordia-hub",
      childCreator: r.child_creator,
      amountCC: Number(r.amount_cc || 0),
      payoutTs: r.payout_ts,
      createdAt: r.created_at,
    }));
    const totalRoyaltyCC = flows.reduce((sum, f) => sum + f.amountCC, 0);
    return { flows, totalRoyaltyCC, generatedAt: Date.now() };
  } catch (err) {
    logger.warn?.("cross-world-feed", "royalty_flow_query_failed", { error: err?.message });
    return { flows: [], totalRoyaltyCC: 0, generatedAt: Date.now() };
  }
}
